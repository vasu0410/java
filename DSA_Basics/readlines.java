import java.util.*;

public class readlines{
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        float b = sc.nextFloat();
        String s = sc.nextLine();

        System.out.println(a);
        System.out.println(b);
        System.out.println(s);

    }
}